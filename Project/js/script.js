$(document).ready(function(){
	$(window).scroll(function () {
			if ($(this).scrollTop() > 50) {
				$('#back-to-top').fadeIn();
			} else {
				$('#back-to-top').fadeOut();
			}
		});
		// scroll body to 0px on click
		$('#back-to-top').click(function () {
			$('body,html').animate({
				scrollTop: 0
			}, 400);
			return false;
		});
		// ANIMATE CONTENT
		$('.specialization-1').waypoint(function() {
			$('.specialization-1').addClass('fadeInLeft')
			}, { offset: '50%' });
});